#include <bits/stdc++.h>
using namespace std;

int main() {

    // Låt x vara talet som linn säger högt
    int x;
    cin >> x;

    // Lägg till 23
    x = x + 23;

    // Dividera med 7
    x = x / 7;

    // Ta bort 5
    x = x - 5;

    // x är nu talet som Linn tänkte på!
    cout << x << endl;

    return 0;
}
