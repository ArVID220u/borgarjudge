#include <bits/stdc++.h>
using namespace std;

int main() {

    // Deklarera talet x, som Linn tänker på
    int x;
    
    // Läs in x
    cin >> x;

    // Lägg till 5 till talet
    x = x + 5;

    // Multiplicera talet med 7
    x = 7 * x;

    // Ta bort 23
    x = x - 23;

    // Skriv ut x – talet som Linn ska säga högt
    cout << x << endl;

    return 0;
}
